export const DICTIONARIES: Record<Language, Dict> = { ru, uk, en };

/** Map our app language code to a BCP 47 locale for Intl.DateTimeFormat etc. */
export function localeFor(lang: Language): string {
  switch (lang) {
    case "ru":
      return "ru-RU";
    case "uk":
      return "uk-UA";
    case "en":
      return "en-US";
  }
}
